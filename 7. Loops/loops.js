// for 
console.log("For loop");
for (let i = 0; i < 5; i++) {
    console.log(i);  
  }  

// while loop 
console.log("While loop");
let k = 0;
while (k < 5) {
  console.log(k);
  k++;  // Output: 0, 1, 2, 3, 4
}

// while loop 
console.log("Do While loop");
let l = 0;
do {
  console.log(l);
  l++;
} while (l < 5);  // Output: 0, 1, 2, 3, 4

